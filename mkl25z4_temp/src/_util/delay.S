	.syntax unified
	.thumb

	.global delay

/*
 *==============================================================================
 * delay
 *==============================================================================
 */

	.section .text.delay, "ax", %progbits
	.type delay, %function
	.thumb_func
delay:

	subs r0, #1
	bne delay
	bx lr

	.size delay, . - delay

/*
 *==============================================================================
 * EOF
 *==============================================================================
 */
	.end
